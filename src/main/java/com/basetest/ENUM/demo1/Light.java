package com.basetest.ENUM.demo1;

/**
 * <pre>
 * 通常定义常量方法
 * 我们通常利用public final static方法定义的代码如下，分别用1表示红灯，3表示绿灯，2表示黄灯。
 * </pre>
 *
 * @author mayanlu
 */
public class Light {
    /* 红灯 */
    public final static int RED = 1;

    /* 绿灯 */
    public final static int GREEN = 3;

    /* 黄灯 */
    public final static int YELLOW = 2;
}